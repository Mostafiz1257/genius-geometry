
// This function use for the input field by there id ............
function getElementById(inputId) {
    const areaValueOfA = document.getElementById(inputId);
    const areaNumber = areaValueOfA.value;
    return areaNumber;
}

// This function use foe the text field by there id...............
function getElementByIdText(inputIdtext) {
    const areaValueOfA = document.getElementById(inputIdtext);
    const areaNumber = areaValueOfA.innerText;
    return areaNumber;
}


